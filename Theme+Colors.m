#import "Theme+Colors.h"

@implementation Theme (Colors)

+ (UIColor *)myKEALightGrey
{
    return [UIColor colorWithRed:0.929 green:0.929 blue:0.929 alpha:1]; /*#ededed or RBG 237-237-237*/
}

+ (UIColor *)myKEABorderColor
{
    return [UIColor colorWithRed:0.596 green:0.596 blue:0.596 alpha:1]; /*#989898 or RGB 152-152-152*/
}

@end
